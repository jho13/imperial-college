//FUNCTION WHICH PRODUCES THE MULTITAP ENCODING OF A PLAINTEXT
//INPUT STRING "plaintext" AND STORES IT IN THE STRING PARAMETER
//"multitap".
void encode(const char* plaintext, char* multitap);
