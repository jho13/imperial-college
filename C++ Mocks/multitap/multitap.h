//FUNCTION WHICH PRODUCES THE MULTITAP ENCODING OF A SINGLE
//INPUT CHARACTER "ch", AND STORES IT IN THE STRING PARAMETER
//"multitap". RETURNS THE LENGTH OF THE MULTITAP ENCODING.
