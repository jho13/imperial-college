//FUNCTION WHICH COPIES "str1" INTO "str2" BACKWARDS.
void reverse(const char* str1, char* str2);

//FUNCTION WHICH RETURNS 1 IF "one" AND "two" ARE THE SAME,
//0 OTHERWISE. IGNORES PUNCTUATIONS AND LETTER CASE.
int compare(const char* one, const char* two);

//FUNCTION WHICH RETURNS 1 IF "sentence" IS A PALINDROME,
//0 OTHERWISE.
int palindrome(const char* sentence);

//FUNCTION WHICH RETURNS 1 IF "str2" IS AN ANAGRAM OF "str1",
//0 OTHERWISE.
int anagram(const char* str1, const char* str2);
